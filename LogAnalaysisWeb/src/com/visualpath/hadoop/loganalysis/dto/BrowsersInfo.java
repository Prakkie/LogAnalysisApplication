package com.visualpath.hadoop.loganalysis.dto;

public class BrowsersInfo {
	
	long chrome;
	long firefox;
	long safari;
	long ie;
	long gecko;
	long appleWebKit;
	public long getChrome() {
		return chrome;
	}
	public void setChrome(long chrome) {
		this.chrome = chrome;
	}
	public long getFirefox() {
		return firefox;
	}
	public void setFirefox(long firefox) {
		this.firefox = firefox;
	}
	public long getSafari() {
		return safari;
	}
	public void setSafari(long safari) {
		this.safari = safari;
	}
	public long getIe() {
		return ie;
	}
	public void setIe(long ie) {
		this.ie = ie;
	}
	public long getGecko() {
		return gecko;
	}
	public void setGecko(long gecko) {
		this.gecko = gecko;
	}
	public long getAppleWebKit() {
		return appleWebKit;
	}
	public void setAppleWebKit(long appleWebKit) {
		this.appleWebKit = appleWebKit;
	}
	
	
	

}
